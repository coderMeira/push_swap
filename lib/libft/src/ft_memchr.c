#include "../inc/libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*src;
	unsigned char	cc;
	int				i;

	i = 0;
	cc = c + 0;
	src = (unsigned char *)s;
	while (n--)
	{
		if (src[i] == cc)
			return (src + i);
		i++;
	}
	return (NULL);
}
