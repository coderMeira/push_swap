#include "../inc/libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	int		s_len;
	char	*p;

	s_len = ft_strlen(s);
	i = 0;
	if (!s || !f)
		return (NULL);
	p = (char *)malloc((s_len + 1) * sizeof(char));
	if (!p)
		return (NULL);
	while (s[i] != '\0')
	{
		p[i] = f(i, s[i]);
		i++;
	}
	p[i] = '\0';
	return (p);
}
