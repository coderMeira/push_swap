#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char nl;
	char *n;

	nl = '\n';
	n = &nl;
	if (!s || fd < 0)
		return ;
	while (*s)
		write(fd, s++, 1);
	write(fd, n, 1);
}
