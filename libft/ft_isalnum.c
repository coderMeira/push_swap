#include "libft.h"

int		ft_isalnum(int c)
{
	if (c > 64 && c < 91)
		return (1);
	else if (c > 96 && c < 123)
		return (1);
	else if (c >= '0' && c <= '9')
		return (1);
	else
		return (0);
}
